export const getStationByName = (name: string) => {
  return mumbaiStations.find(station => station.name === name);
};

export const calculateDistance = (station1: string, station2: string): number => {
  const s1 = getStationByName(station1);
  const s2 = getStationByName(station2);
  
  if (!s1 || !s2) return 0;
  
  // Haversine formula to calculate distance between two points
  const R = 6371; // Earth radius in km
  const dLat = (s2.lat - s1.lat) * Math.PI / 180;
  const dLon = (s2.lng - s1.lng) * Math.PI / 180;
  const a = 
    Math.sin(dLat/2) * Math.sin(dLat/2) +
    Math.cos(s1.lat * Math.PI / 180) * Math.cos(s2.lat * Math.PI / 180) *
    Math.sin(dLon/2) * Math.sin(dLon/2);
  const c = 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1-a));
  const distance = R * c;
  
  return Math.round(distance * 10) / 10; // Round to 1 decimal
};
